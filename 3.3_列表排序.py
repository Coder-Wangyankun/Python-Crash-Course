tourist_locations = ["Japan", "Canada", "America", "DaLian", "HangZhou"]
print(tourist_locations)
print(sorted(tourist_locations))  # 临时排序
print(tourist_locations)
print(sorted(tourist_locations, reverse=True))  # 临时反向排序
print(tourist_locations)
tourist_locations.reverse()
print(tourist_locations)
tourist_locations.reverse()
print(tourist_locations)
tourist_locations.sort()  # 排序
print(tourist_locations)
tourist_locations.sort(reverse=True)  # 反向排序
print(tourist_locations)
